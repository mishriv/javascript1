// Question: Write a JavaScript program that calculates the simple interest using the formula Simple interest = (principal * rate * time) / 100.


// Define the principal amount in dollars
const principal = 1000;

// Define the rate of interest in percentage
const rate = 5; // 5% interest rate

// Define the time period in years
const time = 2;

// Calculate the simple interest using the formula: Simple interest = (principal * rate * time) / 100
const simpleInterest = (principal * rate * time) / 100;

