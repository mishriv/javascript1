// question:  Write a Javascript program to calculate the Body Mass Index (BMI) using the formula BMI = weight (kg)/height * height.


// Define weight in kilograms
const weightKg = 65;

// Define height in meters
const heightM = 1.75;

// Calculate BMI using the formula: BMI = weight (kg) / height (m) * height (m)
const bmi = weightKg / (heightM * heightM);

// Display the result
console.log("The Body Mass Index (BMI) is:", bmi);
